#include <stdio.h>

int main()
{
    int i;
    float f;
    char c;

    // Input integer
    printf("Enter an integer: ");
    scanf("%d", &i);

    // Input float
    printf("Enter a float: ");
    scanf("%f", &f);

    // Input character
    printf("Enter a character: ");
    scanf(" %c", &c); // Space before %c to consume any leftover newline

    // Output the values
    printf("\nYou entered:\n");
    printf("Integer: %d\n", i);
    printf("Float: %.2f\n", f);
    printf("Character: %c\n", c);

    return 0;
}
